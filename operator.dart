void main() {
  var a = 10;
  var b = 20;
  var c = a + b;

  print(c);

  //penjumlahan
  var penjumlahan = a + b;

  //pengurangan
  var pengurangan = a - b;

  //perkalian
  var perkalian = a * b;

  //pembagian
  var pembagian = a / b;

  //sisa bagi
  var sisa = a % b;

  print(penjumlahan);
  print(pengurangan);
  print(perkalian);
  print(pembagian);
  print(sisa);

  print("Relational operator");
  print(a < b);
  print(a > b);
  print(a == b);
  print(a != b);
  print(a >= b);
  print(a <= b);

  print("logical operator");
  bool x = true;
  bool y = false;

  print(x && y); //and
  print(x || y); //or
  print(!x); //not
}
